# На вход список li и число n
# Возвращает кол-во элементов равных n в списке li

def count_element(arr, n):
    counter = 0
    for el in arr:
        if el == n:
            counter += 1
    print(f"Количество элементов равных {n} {counter} штук")

array = [1,4,6,74,2,346,78,8,54,634568,54,63,5,243,4,6,57,45,6,24,14,75,67,35,42,4,6,7854,6,345,24,6,4,673,5,2,67]

n = int(input("Введите искомое число n\n"))
count_element(array, n)